# set operations

s1 = {'red','blue','green','yellow'}
s2 = {'green','yellow','cyan','purple'}

print(s1)
print(s2)
print("union of s1 and s2")
print(s1.union(s2))

print("intersection of s1 and s2")
print(s1.intersection(s2))

print("difference of s1 and s2")
print(s1.difference(s2))
